"""Generates `stdlib/std.fasteners/records/*.toml` from the committed
ISO dimension table `tools/stdlib/data/iso_fasteners.toml` (WO-66,
D174 sourcing law rule 3: standard-table families are GENERATED, not
hand-typed). Deterministic: same input table -> byte-identical output
(no wall-clock, no randomness, sorted rows).
"""

from __future__ import annotations

import tomllib
from pathlib import Path

from tools.stdlib.render import render_records_file

# frob:doc docs/modules/tools.md#stdlib-gen-fasteners
REPO_ROOT = Path(__file__).resolve().parents[2]
# frob:doc docs/modules/tools.md#stdlib-gen-fasteners
DATA_FILE = REPO_ROOT / "tools" / "stdlib" / "data" / "iso_fasteners.toml"
# frob:doc docs/modules/tools.md#stdlib-gen-fasteners
OUT_DIR = REPO_ROOT / "stdlib" / "std.fasteners" / "records"

_LENGTH_NOTE = (
    "generated by tools/stdlib/gen_fasteners.py from a representative "
    "common-stock length subset (8-50mm), NOT the standard's full "
    "per-diameter min/max length range (that per-diameter table was "
    "not independently re-verified this session and is omitted "
    "rather than guessed)"
)


def _class_suffix(cls: str) -> str:
    return cls.replace(".", "_")


# frob:waive PERF004 reason="one-shot sort of a small set, never re-sorted"
def _socket_head_cap_screws(data: dict) -> list[dict]:
    rows = []
    for entry in data["iso_4762"]:
        d = entry["d"]
        for length in data["standard_length_series_mm"]:
            if length < d * 1.5:
                continue  # shorter than the head diameter reads as not-real stock
            for cls, props in data["property_class"].items():
                key = f"shcs_iso4762_m{d:g}x{length:g}_{_class_suffix(cls)}"
                rows.append(
                    {
                        "key": key,
                        "standard": "ISO 4762",
                        "nominal_dia_mm": d,
                        "pitch_mm": entry["pitch"],
                        "length_mm": float(length),
                        "head_dia_mm": entry["dk"],
                        "head_height_mm": entry["k"],
                        "socket_size_mm": entry["s"],
                        "property_class": cls.replace("_", "."),
                        "proof_stress_mpa": props["proof_stress_mpa"],
                        "yield_mpa": props["yield_mpa"],
                        "tensile_min_mpa": props["tensile_min_mpa"],
                        "evidence": {
                            "method": "catalog",
                            "trust_tier": "community",
                            "reference": (
                                "ISO 4762:2004 (hexagon socket head cap "
                                "screws) dimension table + ISO 898-1:2013 "
                                f"property class {cls.replace('_', '.')} "
                                f"mechanical properties; {_LENGTH_NOTE}"
                            ),
                        },
                    }
                )
    return sorted(rows, key=lambda r: r["key"])


