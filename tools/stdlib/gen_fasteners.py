# frob:doc docs/modules/tools.md#stdlib-gen-fasteners
def generate() -> dict[str, str]:
    """Returns {relative_output_path: rendered_content} for every
    std.fasteners records file this generator owns (in-memory; the
    caller decides whether to write to disk or diff against committed
    content -- shared by `make stdlib-gen` and the drift-check test).
    """
    with DATA_FILE.open("rb") as f:
        data = tomllib.load(f)

    families = {
        "socket_head_cap_screws.toml": (
            "component",
            _socket_head_cap_screws(data),
            "ISO 4762 socket head cap screws, generated per-size x "
            "per-length x per-property-class grid.",
        ),
        "hex_bolts.toml": (
            "component",
            _hex_bolts(data),
            "ISO 4014 hexagon head bolts, generated per-size x "
            "per-length x per-property-class grid.",
        ),
        "hex_nuts.toml": (
            "component",
            _hex_nuts(data),
            "ISO 4032 hexagon nuts, generated per-size x per-bolt-property-class grid.",
        ),
        "plain_washers.toml": (
            "component",
            _plain_washers(data),
            "ISO 7089 plain washers, generated per-size grid.",
        ),
    }

    out: dict[str, str] = {}
    for filename, (kind, rows, comment) in families.items():
        content = render_records_file(
            script="tools/stdlib/gen_fasteners.py",
            source="tools/stdlib/data/iso_fasteners.toml",
            header_comment=f"# {comment}",
            kind=kind,
            rows=rows,
        )
        out[str(OUT_DIR / filename)] = content
    return out


# frob:doc docs/modules/tools.md#stdlib-gen-fasteners
# frob:waive TEST001 reason="CLI entry point; see tests/tools/test_stdlib_gen_drift.py"
# frob:waive TEST005 reason="measured 16.7% branch on 2026-07-19; backfill T-0036"
# frob:waive PERF002 reason="one-shot index/count over a small per-call set"
def main() -> None:
    for path_str, content in generate().items():
        path = Path(path_str)
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(content, encoding="ascii")
        print(f"wrote {path.relative_to(REPO_ROOT)} ({content.count(chr(10))} lines)")


if __name__ == "__main__":
    main()
