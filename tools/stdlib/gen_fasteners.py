# frob:waive PERF004 reason="one-shot sort of a small set, never re-sorted"
def _hex_nuts(data: dict) -> list[dict]:
    rows = []
    for entry in data["iso_4032"]:
        d = entry["d"]
        for cls in data["property_class"]:
            # Nuts pair to a bolt property class per ISO 898-2; the
            # simplest, well-established nut-class label is used (no
            # separate nut proof-load table transcribed here -- that
            # per-nut-class table was not independently re-verified
            # this session and is omitted rather than guessed).
            key = f"hexnut_iso4032_m{d:g}_{_class_suffix(cls)}"
            rows.append(
                {
                    "key": key,
                    "standard": "ISO 4032",
                    "nominal_dia_mm": d,
                    "pitch_mm": entry["pitch"],
                    "across_flats_mm": entry["s"],
                    "height_mm": entry["m"],
                    "bolt_property_class": cls.replace("_", "."),
                    "evidence": {
                        "method": "catalog",
                        "trust_tier": "community",
                        "reference": (
                            "ISO 4032:2012 (hexagon nuts, style 1) "
                            "dimension table; paired bolt property class "
                            "per ISO 898-2 nut/bolt pairing convention "
                            "(nut proof-load table itself not "
                            "independently re-verified and omitted)"
                        ),
                    },
                }
            )
    return sorted(rows, key=lambda r: r["key"])


